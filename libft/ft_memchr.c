#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*haystack;
	unsigned char	needle;

	haystack = (unsigned char *)s;
	needle = (unsigned char)c;
	while (n--)
	{
		if (*haystack == needle)
			return (haystack);
		haystack++;
	}
	return (NULL);
}
